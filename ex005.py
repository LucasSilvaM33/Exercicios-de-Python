'''Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e antecessor.'''

num = int(input('Digite um número inteiro qualquer e descubra o seu sucessor e antecessor: '))
sucessor = num + 1
antecessor = num - 1
print(f'O número escolhido foi {num}, seu sucessor é {sucessor} e seu antecessor é {antecessor}')