'''Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada'''

num = float(input('Digite um número qualquer: '))
dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)
print(f'O número escolhido foi {num}, o seu dobro é {dobro}, seu triplo é {triplo} e a sua raiz quadrada é {raiz}')